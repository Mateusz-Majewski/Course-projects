public class Desk {

    private String color;
    private int width;
    private int height;
    private int length;

    public Desk(String color, int width, int height, int length) {
        this.color = color;
        this.width = width;
        this.height = height;
        this.length = length;
    }



    public String getColor() {
        return color;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getLength() {
        return length;
    }
}
